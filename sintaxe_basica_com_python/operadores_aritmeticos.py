# O que são?

# Os operadores aritméticos executam operações matemáticas, como adição, subtração com operandos.

# Adição: +
# Exemplo:
a = 10
b = 20
print(a + b)

# Subtração: -
# Exemplo: 
a = 10
b = 20
print(a - b)

# Multiplicação: *
# Exemplo: 
a = 10
b = 20
print(a * b)

# Divisão: /
# Exemplo: 
a = 10
b = 20
print(a / b)

# Divisão inteira: //
# Exemplo: 
a = 10
b = 20
print(a // b)

# Módulo: %
# Exemplo: 
a = 10
b = 20
print(a % b)

# Exponenciação: **
# Exemplo: 
a = 10
b = 20
print(a ** b)

# Na matemática

# Na matemática existe uma regra que indica quais operações devem ser execultadas preimeiro. 
# Isso é útil pois ao analisar uma expressão, a depender da ordem das operações o valor pode ser diferente:

# Exemplo: 
a = 10
b = 20
c = 30
print(a + b * c)

# A definição indica a seguinte ordem como a correta:
# 1. Parênteses
# 2. Expoentes
# 3. Multiplicação e divisão
# 4. Adição e subtração

# Exemplo: 
a = 10
b = 20
c = 30
print((a + b) * c)

