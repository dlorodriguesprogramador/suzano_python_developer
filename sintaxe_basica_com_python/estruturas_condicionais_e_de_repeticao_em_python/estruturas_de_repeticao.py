# Estruturas de repetição

# O que são?

# São estruturas que permitem que o programa execute um bloco de código repetidamente.

# Exemplo:

for i in range(10):
    print(i)

# O que é o for?

# O for é uma estrutura de repetição que executa um bloco de código um número determinado de vezes.

# Exemplo:

for i in range(10):
    print(i)

# O que é o range?

# O range é uma função que retorna uma sequência de números.

# Exemplo:

range(10)

# Exercício:

# Crie um programa que receba um número e imprima a tabuada desse número.

numero = int(input("Digite um número: "))

for i in range(10):
    print(f"{numero} x {i} = {numero * i}")



