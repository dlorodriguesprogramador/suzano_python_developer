# O que são?

# São operadores utilizados para verificar se duas variáveis ocupam a mesma posição de memória.

# Operador is:
# Exemplo: 
a = 10
b = 10
print(a is b)

# Operador is not:
# Exemplo: 
a = 10
b = 20
print(a is not b)

# Exercício:

a = 10
b = 20
c = 30  

print(a is b)
print(a is not b)
print(a is c)
print(a is not c)
print(b is c)
print(b is not c)