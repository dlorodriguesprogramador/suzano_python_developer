# Introdução

# Fatiamento de strings é uma técnica utilizada para retornar substrings 
# (partes da string original), informando o inicio (start), o fim (end) e o passo (step).

# Fatiamento

nome = 'Guilherme Arthur de Carvalho'

print(nome[0])

print(nome[:9])

print(nome[10:])

print(nome[10:16])

print(nome[10:16:2])

print(nome[::-1])