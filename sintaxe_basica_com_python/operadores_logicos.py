# O que são?

# São operadores utilizados em conjunto com os operadores de comparação para criar expressões lógicas. 
# Quando uma expressão lógica é verdadeira, o resultado é True, e quando é falsa, o resultado é False.

# Operador AND:
# Exemplo: 
a = 10
b = 20
c = 30
print(a < b and b < c)

# Operador OR:
# Exemplo: 
a = 10
b = 20
c = 30
print(a < b or b > c)

# Operador NOT:
# Exemplo: 
a = 10
b = 20
c = 30
print(not a < b)

