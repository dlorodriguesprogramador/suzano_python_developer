# O que são?

# São operadores utilizados para comparar dois valores.

# Igualdade: ==
# Exemplo: 
a = 10
b = 20
print(a == b)

# Diferença: !=
# Exemplo: 
a = 10
b = 20
print(a != b)

# Maior que: >
# Exemplo: 
a = 10
b = 20
print(a > b)

# Menor que: <
# Exemplo: 
a = 10
b = 20
print(a < b)

# Maior ou igual a: >=
# Exemplo: 
a = 10
b = 20
print(a >= b)

# Menor ou igual a: <=
# Exemplo: 
a = 10
b = 20
print(a <= b)

