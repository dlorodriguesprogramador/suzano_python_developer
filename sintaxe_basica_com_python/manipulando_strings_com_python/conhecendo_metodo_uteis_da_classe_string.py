# Objetivo Geral

# Conhecer métodos úteis para manipular objetos do tipo string, 
# como interpolar valores de cariáveis e entender como funciona o fatiamento.

# Maiúscula, minúscula e título.

# Exemplo:

nome = 'Guilherme'

print(nome.upper())

print(nome.lower())

print(nome.title())

# Eliminando espaços em branco.

texto = '  Python  '

print(texto.strip())

print(texto.lstrip())

print(texto.rstrip())

# Junções e centralização.

nome = 'Guilherme'

print(nome.join(' '))

print(nome.center(10, '#'))